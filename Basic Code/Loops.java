public class Loops {
    public static void main(String args[]){

        //While Loops
        int wh=1;
        while(wh<5){
            System.out.println(wh);
            wh++;
        }

        //Do-While Loops
        int d=1;
        do{
            System.out.println(d);
            d++;
        }while(d<5);

        //For-Loops
    
        for(int f=1;f<5;f++){
            System.out.println(f);
        }





    }
}
